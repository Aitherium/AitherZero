set -euo pipefail

echo "════════════════════════════════════════════════════════════════════════"
echo "  AitherMesh Provisioning Script"
echo "════════════════════════════════════════════════════════════════════════"

MODE="$1"
GPU_ENABLED="$2"
REGISTRY="$3"

# Detect OS
if [ -f /etc/os-release ]; then
    . /etc/os-release
    OS=$ID
else
    OS="unknown"
fi

echo "[1/5] Detected OS: $OS"

# Install Docker/Podman
echo "[2/5] Installing container runtime..."
if command -v podman &> /dev/null; then
    echo "  Podman already installed"
    RUNTIME="podman"
elif command -v docker &> /dev/null; then
    echo "  Docker already installed"
    RUNTIME="docker"
else
    case "$OS" in
        ubuntu|debian)
            apt-get update
            apt-get install -y podman
            RUNTIME="podman"
            ;;
        fedora|centos|rhel|rocky|almalinux)
            dnf install -y podman
            RUNTIME="podman"
            ;;
        *)
            echo "Installing Docker..."
            curl -fsSL https://get.docker.com | sh
            RUNTIME="docker"
            ;;
    esac
fi
echo "  Using: $RUNTIME"

# Install NVIDIA drivers if GPU enabled
if [ "$GPU_ENABLED" = "true" ]; then
    echo "[3/5] Installing NVIDIA drivers..."
    if command -v nvidia-smi &> /dev/null; then
        echo "  NVIDIA drivers already installed"
        nvidia-smi --query-gpu=name --format=csv,noheader
    else
        case "$OS" in
            ubuntu|debian)
                apt-get install -y nvidia-driver-535 nvidia-container-toolkit
                ;;
            fedora|centos|rhel|rocky|almalinux)
                dnf install -y nvidia-driver nvidia-container-toolkit
                ;;
        esac
    fi
    
    # Configure container runtime for GPU
    if [ "$RUNTIME" = "podman" ]; then
        nvidia-ctk runtime configure --runtime=podman || true
    else
        nvidia-ctk runtime configure --runtime=docker || true
        systemctl restart docker || true
    fi
else
    echo "[3/5] Skipping GPU setup (not enabled)"
fi

