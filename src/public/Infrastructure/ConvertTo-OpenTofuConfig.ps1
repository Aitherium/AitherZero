# ══════════════════════════════════════════════════════════════════════════════
# Internal: HCL value formatting
# ══════════════════════════════════════════════════════════════════════════════

function _Format-HCLValue {
    param($Value)

    if ($Value -is [bool]) {
        return if ($Value) { 'true' } else { 'false' }
    }
    if ($Value -is [int] -or $Value -is [long] -or $Value -is [double]) {
        return "$Value"
    }
    if ($Value -is [string]) {
        return "`"$Value`""
    }
    if ($Value -is [array]) {
        $Items = $Value | ForEach-Object { _Format-HCLValue $_ }
        return "[$($Items -join ', ')]"
    }
    if ($Value -is [hashtable] -or $Value -is [System.Collections.Specialized.OrderedDictionary]) {
        $Entries = $Value.GetEnumerator() | ForEach-Object {
            "    $($_.Key) = $(_Format-HCLValue $_.Value)"
        }
        return "{`n$($Entries -join "`n")`n  }"
    }

    # PSCustomObject
    if ($Value -is [PSCustomObject]) {
        $Hash = @{}
        $Value.PSObject.Properties | ForEach-Object { $Hash[$_.Name] = $_.Value }
        return _Format-HCLValue $Hash
    }

    return "`"$Value`""
}


function _Convert-HCLToJSON {
    param([string]$HCL)
    # Simple HCL-to-JSON wrapper — for full fidelity use hcl2json
    # This returns the raw HCL wrapped in a JSON structure for tools that prefer JSON
    return @{
        _comment = 'HCL content — use hcl2json for full conversion'
        raw_hcl  = $HCL
    } | ConvertTo-Json -Depth 5
}

