        pip3 install httpx fastapi uvicorn pyyaml
        # Download mesh agent
        curl -sL "https://raw.githubusercontent.com/Aitherium/AitherZero/main/AitherOS/AitherNode/services/mesh/AitherMesh.py" \
            -o /usr/local/bin/aither-mesh-agent.py || true
        chmod +x /usr/local/bin/aither-mesh-agent.py 2>/dev/null || true
    fi
}

echo ""
echo "════════════════════════════════════════════════════════════════════════"
echo "  AitherMesh installation complete!"
echo "  Run: aither-mesh-agent join --controller auto"
echo "════════════════════════════════════════════════════════════════════════"
'@

