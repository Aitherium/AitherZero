# Internal: IntentGraph resource type → module mapping
# ══════════════════════════════════════════════════════════════════════════════

function _Get-ModuleMapping {
    @{
        # AWS resources → library modules
        'ec2:instance'        = @{ module = 'compute';    var_map = @{ services = 'map'; vpc_id = 'ref'; subnet_ids = 'ref' } }
        'ecs:service'         = @{ module = 'compute';    var_map = @{ services = 'map'; vpc_id = 'ref'; subnet_ids = 'ref' } }
        'eks:cluster'         = @{ module = 'compute';    var_map = @{ services = 'map'; vpc_id = 'ref'; subnet_ids = 'ref' } }
        'vpc:vpc'             = @{ module = 'networking'; var_map = @{ vpc_cidr = 'string'; azs = 'list'; enable_nat = 'bool' } }
        'vpc:subnet'          = @{ module = 'networking'; var_map = @{ vpc_cidr = 'string'; azs = 'list' } }
        'elb:load-balancer'   = @{ module = 'networking'; var_map = @{ vpc_id = 'ref'; subnet_ids = 'ref' } }
        's3:bucket'           = @{ module = 'storage';    var_map = @{ buckets = 'map' } }
        'ebs:volume'          = @{ module = 'storage';    var_map = @{ } }
        'efs:filesystem'      = @{ module = 'storage';    var_map = @{ } }
        'rds:instance'        = @{ module = 'database';   var_map = @{ engine = 'string'; instance_class = 'string'; allocated_storage = 'number' } }
        'rds:aurora'          = @{ module = 'database';   var_map = @{ engine = 'string'; instance_class = 'string' } }
        'elasticache:cluster' = @{ module = 'database';   var_map = @{ engine = 'string' } }
        'dynamodb:table'      = @{ module = 'database';   var_map = @{ } }
        # Docker resources
        'docker:container'    = @{ module = 'containers'; var_map = @{ containers = 'map'; docker_host = 'string' } }
        'docker:compose'      = @{ module = 'containers'; var_map = @{ containers = 'map' } }
        # Kubernetes resources
        'k8s:deployment'      = @{ module = 'deployments'; var_map = @{ namespace = 'string'; deployments = 'map' } }
        'k8s:statefulset'     = @{ module = 'deployments'; var_map = @{ namespace = 'string'; deployments = 'map' } }
        'k8s:service'         = @{ module = 'services';    var_map = @{ namespace = 'string'; services = 'map' } }
    }
}


function _Map-ResourceToModuleVars {
    param($Resource, $ModuleRef, $Environment)

    $Vars = @{}
    $Config = $Resource.config

    switch ($ModuleRef.module) {
        'compute' {
            $ServiceEntry = @{
                image    = $Config.image ?? "ghcr.io/aitherium/$($Resource.name):latest"
                ports    = @($Config.ports ?? @(8080))
                replicas = $Resource.quantity ?? 1
                cpu      = $Config.cpu ?? 256
                memory   = ($Config.memory_gb ?? 1) * 1024
                env      = @{ AITHER_DOCKER_MODE = 'true'; LOG_LEVEL = 'INFO' }
            }
            $Vars['services'] = @{ ($Resource.name) = $ServiceEntry }
        }
        'networking' {
            $Vars['vpc_cidr']    = $Config.cidr ?? '10.0.0.0/16'
            $Vars['enable_nat']  = $Environment -ne 'dev'
        }
        'storage' {
            $BucketEntry = @{
                versioning = $Environment -ne 'dev'
            }
            $Vars['buckets'] = @{ ($Resource.name) = $BucketEntry }
        }
        'database' {
            $Vars['engine']            = $Config.engine ?? 'postgres'
            $Vars['instance_class']    = $Config.instance_type ?? 'db.t3.medium'
            $Vars['allocated_storage'] = $Config.storage_gb ?? 20
        }
        'containers' {
            $ContainerEntry = @{
                image   = $Config.image ?? "ghcr.io/aitherium/$($Resource.name):latest"
                ports   = @(@{ internal = ($Config.port ?? 8080); external = ($Config.port ?? 8080) })
                restart = 'unless-stopped'
                env     = @{ AITHER_DOCKER_MODE = 'true' }
            }
            $Vars['containers'] = @{ ($Resource.name) = $ContainerEntry }
        }
        'deployments' {
            $DeployEntry = @{
                image    = $Config.image ?? "ghcr.io/aitherium/$($Resource.name):latest"
                replicas = $Resource.quantity ?? 1
                ports    = @($Config.port ?? 8080)
                env      = @{ AITHER_DOCKER_MODE = 'true' }
            }
            $Vars['deployments'] = @{ ($Resource.name) = $DeployEntry }
            $Vars['namespace']   = 'aitheros'
        }
        'services' {
            $ServiceDef = @{
                type  = 'ClusterIP'
                ports = @(@{ port = ($Config.port ?? 80); target_port = ($Config.port ?? 8080) })
            }
            $Vars['services']  = @{ ($Resource.name) = $ServiceDef }
            $Vars['namespace'] = 'aitheros'
        }
    }

    return $Vars
}


# ══════════════════════════════════════════════════════════════════════════════
# Internal: Inline HCL generation (when no module exists)
# ══════════════════════════════════════════════════════════════════════════════

function _Generate-InlineResource {
    param($Resource, $Provider, $Environment, $Dependencies)

    $ResType = $Resource.type
    $ResName = $Resource.name -replace '[^a-z0-9_]', '_'
    $Config = $Resource.config
    $Tags = $Resource.tags ?? @{}
    $Quantity = $Resource.quantity ?? 1

    $Main = ''
    $Output = ''
    $Variables = @()

    $TagBlock = ($Tags.GetEnumerator() | ForEach-Object { "    $($_.Key) = `"$($_.Value)`"" }) -join "`n"

    switch -Wildcard ($ResType) {
        'ec2:instance' {
            $Main = @"

resource "aws_instance" "$ResName" {
  count         = $Quantity
  ami           = data.aws_ami.amazon_linux.id
  instance_type = "$($Config.instance_type ?? 't3.medium')"

  tags = {
$TagBlock
    Name = "$ResName-`${count.index}"
  }
}

data "aws_ami" "amazon_linux" {
  most_recent = true
  owners      = ["amazon"]
  filter {
    name   = "name"
    values = ["al2023-ami-*-x86_64"]
  }
}
"@
            $Output = @"

output "${ResName}_ids" {
  value = aws_instance.${ResName}[*].id
}
"@
        }
        's3:bucket' {
            $Main = @"

resource "aws_s3_bucket" "$ResName" {
  bucket = "$($Resource.name)"
  tags = {
$TagBlock
  }
}

resource "aws_s3_bucket_versioning" "$ResName" {
  bucket = aws_s3_bucket.${ResName}.id
  versioning_configuration {
    status = "$(if ($Environment -eq 'dev') { 'Suspended' } else { 'Enabled' })"
  }
}
"@
            $Output = @"

output "${ResName}_arn" {
  value = aws_s3_bucket.${ResName}.arn
}
"@
        }
        'rds:instance' {
            $Main = @"

resource "aws_db_instance" "$ResName" {
  identifier           = "$($Resource.name)"
  engine               = "$($Config.engine ?? 'postgres')"
  instance_class       = "$($Config.instance_type ?? 'db.t3.medium')"
  allocated_storage    = $($Config.storage_gb ?? 20)
  db_name              = "aitheros"
  username             = "aither_admin"
  manage_master_user_password = true
  skip_final_snapshot  = $(if ($Environment -eq 'dev') { 'true' } else { 'false' })
  tags = {
$TagBlock
  }
}
"@
            $Output = @"

output "${ResName}_endpoint" {
  value     = aws_db_instance.${ResName}.endpoint
  sensitive = false
}
"@
        }
        'elasticache:cluster' {
            $Main = @"

resource "aws_elasticache_cluster" "$ResName" {
  cluster_id           = "$($Resource.name)"
  engine               = "$($Config.engine ?? 'redis')"
  node_type            = "$($Config.instance_type ?? 'cache.t3.medium')"
  num_cache_nodes      = $Quantity
  port                 = 6379
  tags = {
$TagBlock
  }
}
"@
        }
        'vpc:vpc' {
            $Main = @"

resource "aws_vpc" "$ResName" {
  cidr_block           = "$($Config.cidr ?? '10.0.0.0/16')"
  enable_dns_hostnames = true
  enable_dns_support   = true
  tags = {
$TagBlock
    Name = "$ResName"
  }
}
"@
            $Output = @"

output "${ResName}_id" {
  value = aws_vpc.${ResName}.id
}
"@
        }
        'docker:container' {
            $PortMappings = if ($Config.ports) {
                ($Config.ports | ForEach-Object {
                    "  ports { internal = $_; external = $_ }"
                }) -join "`n"
            } else {
                '  ports { internal = 8080; external = 8080 }'
            }
            $Main = @"

resource "docker_image" "$ResName" {
  name         = "$($Config.image ?? "ghcr.io/aitherium/$($Resource.name):latest")"
  keep_locally = true
}

resource "docker_container" "$ResName" {
  name  = "$($Resource.name)"
  image = docker_image.${ResName}.image_id
  restart = "unless-stopped"
$PortMappings
  labels {
    label = "managed_by"; value = "opentofu"
  }
  labels {
    label = "aither.project"; value = "aitheros"
  }
}
"@
        }
        'k8s:deployment' {
            $Main = @"

resource "kubernetes_deployment" "$ResName" {
  metadata {
    name      = "$($Resource.name)"
    namespace = "aitheros"
    labels    = { app = "$($Resource.name)"; managed_by = "opentofu" }
  }
  spec {
    replicas = $Quantity
    selector { match_labels = { app = "$($Resource.name)" } }
    template {
      metadata { labels = { app = "$($Resource.name)" } }
      spec {
        container {
          name  = "$($Resource.name)"
          image = "$($Config.image ?? "ghcr.io/aitherium/$($Resource.name):latest")"
          port  { container_port = $($Config.port ?? 8080) }
          resources {
            requests = { cpu = "100m"; memory = "128Mi" }
            limits   = { cpu = "500m"; memory = "$($Config.memory_gb ?? 1)Gi" }
          }
        }
      }
    }
  }
}
"@
        }
        default {
            $Main = @"

# Resource: $ResType ($($Resource.name))
# No inline template available — use Module strategy or extend _Generate-InlineResource
# Config: $($Config | ConvertTo-Json -Compress -Depth 3)
"@
        }
    }

    # Check for depends_on from IntentGraph dependencies
    $DependsOn = @($Dependencies | Where-Object { $_.to -eq $Resource.id } | ForEach-Object { $_.from })
    if ($DependsOn.Count -gt 0 -and $Main -match 'resource ') {
        # Insert depends_on before closing brace
        $DependsOnRefs = ($DependsOn | ForEach-Object { "    # dependency: $_" }) -join "`n"
        $Main = $Main -replace '(}\s*$)', "$DependsOnRefs`n`$1"
    }

    return @{
        main      = $Main
        output    = $Output
        variables = $Variables
    }
}


