#Requires -Version 7.0
<#
.SYNOPSIS
    Creates a GitHub Pull Request using the GitHub CLI.

.DESCRIPTION
    Creates a PR from the current branch to a target branch. Supports
    draft PRs, labels, reviewers, and auto-fill from commit messages.

.PARAMETER Title
    PR title. If omitted, uses the last commit message.

.PARAMETER Body
    PR body/description. If omitted, auto-generates from commit log.

.PARAMETER Base
    Target branch for the PR. Default: develop

.PARAMETER Draft
    Create as a draft PR.

.PARAMETER Labels
    Comma-separated labels to apply.

.PARAMETER Reviewers
    Comma-separated reviewer usernames.

.PARAMETER AutoMerge
    Enable auto-merge when checks pass.

.EXAMPLE
    .\0703_Create-PullRequest.ps1 -Title "feat: ring deployment automation"

.EXAMPLE
    .\0703_Create-PullRequest.ps1 -Title "Promote to staging" -Base staging -Labels "deploy,ring-1"

.NOTES
    Category: git
    Script: 0703
    Requires: GitHub CLI (gh)
#>
[CmdletBinding()]
param(
    [Parameter()]
    [string]$Title,

    [Parameter()]
    [string]$Body,

    [string]$Base = 'develop',

    [switch]$Draft,

    [string]$Labels,

    [string]$Reviewers,

    [switch]$AutoMerge
)

$ErrorActionPreference = 'Stop'

# Verify gh CLI
if (-not (Get-Command gh -ErrorAction SilentlyContinue)) {
    Write-Error "GitHub CLI (gh) is required. Install: https://cli.github.com"
}

# Current branch
$currentBranch = git symbolic-ref --short HEAD
if ($currentBranch -eq $Base) {
    Write-Error "Cannot create PR from '$currentBranch' to itself. Switch to a feature branch first."
}

# Auto-fill title from commits
if (-not $Title) {
    $Title = git log "origin/$Base..$currentBranch" --pretty=format:"%s" | Select-Object -First 1
    if (-not $Title) { $Title = "PR from $currentBranch" }
    Write-Host "  Auto-title: $Title" -ForegroundColor DarkGray
}

# Auto-fill body from commit log
if (-not $Body) {
    $commits = git log "origin/$Base..$currentBranch" --pretty=format:"- %s" 2>$null
    if ($commits) {
        $Body = "## Changes`n`n$($commits -join "`n")`n`n---`n*Auto-generated by AitherZero*"
    } else {
        $Body = "PR from ``$currentBranch`` → ``$Base``"
    }
}

# Build gh command
$ghArgs = @('pr', 'create', '--title', $Title, '--body', $Body, '--base', $Base)

if ($Draft) { $ghArgs += '--draft' }
if ($Labels) { $ghArgs += '--label'; $ghArgs += $Labels }
if ($Reviewers) { $ghArgs += '--reviewer'; $ghArgs += $Reviewers }

# Ensure branch is pushed
Write-Host "  Ensuring branch is pushed..." -ForegroundColor Gray
git push origin $currentBranch 2>&1 | Out-Null

# Create PR
Write-Host "  Creating PR: $Title" -ForegroundColor Cyan
Write-Host "  $currentBranch → $Base" -ForegroundColor DarkGray

$result = & gh @ghArgs 2>&1
$prUrl = $result | Select-String -Pattern 'https://github.com' | Select-Object -First 1

if ($prUrl) {
    Write-Host "  ✓ PR created: $prUrl" -ForegroundColor Green
} else {
    Write-Host "  Result: $result" -ForegroundColor Yellow
}

# Enable auto-merge if requested
if ($AutoMerge -and $prUrl) {
    $prNumber = ($prUrl -split '/')[-1]
    gh pr merge $prNumber --auto --squash 2>&1
    Write-Host "  ✓ Auto-merge enabled" -ForegroundColor Green
}
