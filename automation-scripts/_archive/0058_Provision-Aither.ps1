<#
.SYNOPSIS
    Deploy AitherClient or AitherNode to a bare metal machine.

.DESCRIPTION
    This script provisions a new machine to join the AitherMesh by:
    1. Connecting via SSH/WinRM to the target machine
    2. Installing prerequisites (Docker/Podman, NVIDIA drivers if GPU)
    3. Deploying AitherClient or AitherNode
    4. Configuring mesh.conf with PSK and controller URL
    5. Starting the mesh agent to auto-join

    Supports both Windows and Linux targets.

.PARAMETER Target
    Target machine hostname or IP address

.PARAMETER Mode
    Deployment mode: "Client" (lightweight) or "Node" (full)

.PARAMETER MeshPSK
    Pre-shared key for mesh authentication

.PARAMETER Controller
    Controller URL or "auto" for discovery

.PARAMETER Services
    List of services to enable (for Node mode)

.PARAMETER GPUEnabled
    Enable GPU support (installs NVIDIA drivers)

.PARAMETER Credential
    PSCredential for SSH/WinRM authentication

.PARAMETER SSHKeyPath
    Path to SSH private key for Linux targets

.PARAMETER Role
    Node role: client, worker, edge

.EXAMPLE
    # Deploy lightweight client
    ./0058_Provision-Aither.ps1 -Target "192.168.1.100" -Mode Client -MeshPSK $env:AITHER_MESH_PSK

.EXAMPLE
    # Deploy full node with specific services
    ./0058_Provision-Aither.ps1 -Target "gpu-server.local" -Mode Node `
        -Services @("AitherParallel", "Ollama") -GPUEnabled -MeshPSK $env:AITHER_MESH_PSK

.EXAMPLE
    # Deploy with SSH key
    ./0058_Provision-Aither.ps1 -Target "ubuntu@192.168.1.50" -Mode Client `
        -SSHKeyPath "~/.ssh/id_ed25519" -MeshPSK $env:AITHER_MESH_PSK

.NOTES
    Author: Aitherium
    Version: 1.0.0
    Priority: P390 / P534
    Section: §71 DOM Deploy / §78 AitherProtocol
#>

[CmdletBinding(SupportsShouldProcess)]
param(
    [Parameter(Mandatory, Position = 0)]
    [string]$Target,

    [Parameter()]
    [ValidateSet("Client", "Node")]
    [string]$Mode = "Client",

    [Parameter()]
    [string]$MeshPSK = $env:AITHER_MESH_PSK,

    [Parameter()]
    [string]$Controller = "auto",

    [Parameter()]
    [string[]]$Services = @(),

    [Parameter()]
    [switch]$GPUEnabled,

    [Parameter()]
    [PSCredential]$Credential,

    [Parameter()]
    [string]$SSHKeyPath,

    [Parameter()]
    [ValidateSet("client", "worker", "edge")]
    [string]$Role = "client",

    [Parameter()]
    [string]$ContainerRegistry = "ghcr.io/aitherium",

    [Parameter()]
    [switch]$Force,

    [Parameter()]
    [switch]$DryRun
)

