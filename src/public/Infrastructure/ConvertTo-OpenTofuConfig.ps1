#Requires -Version 7.0
Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'

<#
.SYNOPSIS
    Convert an IDI IntentGraph into production-ready OpenTofu HCL configuration.

.DESCRIPTION
    ConvertTo-OpenTofuConfig is the bridge between the IDI intent pipeline and
    real OpenTofu infrastructure provisioning. It takes an IntentGraph DAG
    (from ConvertTo-IntentGraph) and generates:

    - Provider blocks with version constraints
    - Resource blocks with full attribute mappings
    - Variable definitions with defaults and validation
    - Output definitions for cross-module references
    - Backend configuration for remote state
    - Module composition (references existing library/infrastructure/modules)
    - Dependency ordering via depends_on and data sources

    Generation strategies:
    - Module: Reference existing AitherZero infra modules (preferred)
    - Inline: Generate standalone resource blocks (for custom resources)
    - Hybrid: Mix modules and inline for resources not covered by modules

    The generated HCL is compatible with:
    - Invoke-AitherInfra (plan/apply against workspace)
    - AitherVeil TerraformGenerator (same variable naming conventions)
    - Genesis /infra/requests (commit → plan → approve → apply)
    - library/infrastructure/modules/* (module sources)

.PARAMETER IntentGraph
    The IntentGraph from ConvertTo-IntentGraph.

.PARAMETER OutputPath
    Directory to write generated .tf files. If omitted, returns HCL as string.

.PARAMETER Strategy
    Generation strategy: Module, Inline, or Hybrid. Default: Module.

.PARAMETER Backend
    State backend: local, s3, azurerm, gcs. Default: local.

.PARAMETER BackendConfig
    Backend-specific config hashtable.

.PARAMETER IncludeVariables
    Generate variables.tf with all extracted variables. Default: true.

.PARAMETER IncludeOutputs
    Generate outputs.tf with resource references. Default: true.

.PARAMETER ModuleSourceBase
    Base path for module sources. Default: ../../modules (relative to env dir).

.PARAMETER Format
    Output format: HCL (native .tf) or JSON (.tf.json). Default: HCL.

.PARAMETER DryRun
    Show what would be generated without writing files.

.PARAMETER PassThru
    Return the generation result object.

.EXAMPLE
    $Graph = ConvertTo-IntentGraph -Intent "Deploy 3 Redis nodes in us-east-1"
    $Graph | ConvertTo-OpenTofuConfig -OutputPath ./environments/dev

.EXAMPLE
    $Graph | ConvertTo-OpenTofuConfig -Strategy Inline -Format JSON -PassThru

.NOTES
    Part of AitherZero Infrastructure pipeline.
    Copyright © 2025-2026 Aitherium Corporation.
#>
function ConvertTo-OpenTofuConfig {
    [CmdletBinding(SupportsShouldProcess)]
    [OutputType([PSCustomObject])]
    param(
        [Parameter(Mandatory, ValueFromPipeline)]
        [PSCustomObject]$IntentGraph,

        [string]$OutputPath,

        [ValidateSet('Module', 'Inline', 'Hybrid')]
        [string]$Strategy = 'Module',

        [ValidateSet('local', 's3', 'azurerm', 'gcs')]
        [string]$Backend = 'local',

        [hashtable]$BackendConfig = @{},

        [bool]$IncludeVariables = $true,

        [bool]$IncludeOutputs = $true,

        [string]$ModuleSourceBase = '../../modules',

        [ValidateSet('HCL', 'JSON')]
        [string]$Format = 'HCL',

        [switch]$DryRun,

        [switch]$PassThru
    )

    process {
        $Provider = $IntentGraph.provider
        $Environment = $IntentGraph.environment
        $Resources = @($IntentGraph.resources)
        $Dependencies = @($IntentGraph.dependencies)

        Write-Host "`n  📝 Generating OpenTofu Configuration" -ForegroundColor Cyan
        Write-Host "  Strategy:    $Strategy" -ForegroundColor Gray
        Write-Host "  Provider:    $Provider" -ForegroundColor Gray
        Write-Host "  Resources:   $($Resources.Count)" -ForegroundColor Gray
        Write-Host "  Environment: $Environment" -ForegroundColor Gray

        # ── Provider → module mapping ─────────────────────────────────────
        $ProviderConfig = _Get-ProviderConfig -Provider $Provider
        $ModuleMap = _Get-ModuleMapping

        # ── Generate HCL blocks ───────────────────────────────────────────
        $MainBlocks = [System.Collections.Generic.List[string]]::new()
        $VariableBlocks = [System.Collections.Generic.List[string]]::new()
        $OutputBlocks = [System.Collections.Generic.List[string]]::new()
        $ModulesUsed = @{}
        $VariablesDefined = @{}

        # Header comment
        $MainBlocks.Add(@"
# =============================================================================
# AitherOS Infrastructure — $($Environment.ToUpper())
# =============================================================================
# Auto-generated by ConvertTo-OpenTofuConfig from IDI IntentGraph
# Intent: $($IntentGraph.intent)
# Hash:   $($IntentGraph.intent_hash)
# Generated: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss UTC')
# Strategy: $Strategy
# =============================================================================
"@)

        # Terraform block with backend
        $BackendHCL = _Generate-BackendBlock -Backend $Backend -BackendConfig $BackendConfig -Environment $Environment
        $MainBlocks.Add(@"

terraform {
  required_version = ">= 1.6"

  required_providers {
$($ProviderConfig.required_providers)
  }

$BackendHCL
}
"@)

        # Provider block
        $MainBlocks.Add(@"

$($ProviderConfig.provider_block)
"@)

        # ── Process each resource ─────────────────────────────────────────
        foreach ($res in $Resources) {
            $ResType = $res.type         # e.g. 'ec2:instance', 'rds:instance'
            $ResName = $res.name
            $Action = $res.action

            # Skip destroy-only resources in config generation
            if ($Action -eq 'destroy') {
                $MainBlocks.Add("`n# DESTROY target: $ResName ($ResType) — handled by tofu destroy -target")
                continue
            }

            # Find matching module
            $ModuleRef = $ModuleMap[$ResType]

            if ($Strategy -eq 'Module' -and $ModuleRef) {
                # ── Module-based generation ───────────────────────────────
                $ModuleName = $ModuleRef.module
                $ModuleKey = "mod_$($ResName -replace '[^a-z0-9_]', '_')"

                if (-not $ModulesUsed.ContainsKey($ModuleName)) {
                    $ModulesUsed[$ModuleName] = @()
                }
                $ModulesUsed[$ModuleName] += $res

                $ModuleVars = _Map-ResourceToModuleVars -Resource $res -ModuleRef $ModuleRef -Environment $Environment
                $VarLines = ($ModuleVars.GetEnumerator() | ForEach-Object {
                    $val = _Format-HCLValue $_.Value
                    "  $($_.Key) = $val"
                }) -join "`n"

                $MainBlocks.Add(@"

module "$ModuleKey" {
  source      = "$ModuleSourceBase/$ModuleName"
  environment = "$Environment"
$VarLines
}
"@)

                if ($IncludeOutputs) {
                    $OutputBlocks.Add(@"

output "$($ModuleKey)_result" {
  description = "Output from $ModuleKey ($ResType)"
  value       = module.$ModuleKey
}
"@)
                }
            } elseif ($Strategy -eq 'Inline' -or ($Strategy -eq 'Hybrid' -and -not $ModuleRef)) {
                # ── Inline resource generation ────────────────────────────
                $InlineHCL = _Generate-InlineResource -Resource $res -Provider $Provider `
                    -Environment $Environment -Dependencies $Dependencies
                $MainBlocks.Add($InlineHCL.main)

                if ($IncludeOutputs -and $InlineHCL.output) {
                    $OutputBlocks.Add($InlineHCL.output)
                }

                foreach ($varDef in $InlineHCL.variables) {
                    if (-not $VariablesDefined.ContainsKey($varDef.name)) {
                        $VariableBlocks.Add($varDef.hcl)
                        $VariablesDefined[$varDef.name] = $true
                    }
                }
            } else {
                # Hybrid with module available → use module path
                $ModuleName = $ModuleRef.module
                $ModuleKey = "mod_$($ResName -replace '[^a-z0-9_]', '_')"

                $ModuleVars = _Map-ResourceToModuleVars -Resource $res -ModuleRef $ModuleRef -Environment $Environment
                $VarLines = ($ModuleVars.GetEnumerator() | ForEach-Object {
                    $val = _Format-HCLValue $_.Value
                    "  $($_.Key) = $val"
                }) -join "`n"

                $MainBlocks.Add(@"

module "$ModuleKey" {
  source      = "$ModuleSourceBase/$ModuleName"
  environment = "$Environment"
$VarLines
}
"@)
            }
        }

        # ── Standard variables ────────────────────────────────────────────
        if ($IncludeVariables) {
            $StandardVars = @"
# =============================================================================
# Variables — Auto-generated from IDI IntentGraph
# =============================================================================

variable "environment" {
  description = "Deployment environment"
  type        = string
  default     = "$Environment"
  validation {
    condition     = contains(["dev", "staging", "prod"], var.environment)
    error_message = "Environment must be dev, staging, or prod."
  }
}

variable "project" {
  description = "Project name for resource tagging"
  type        = string
  default     = "aitheros"
}

variable "managed_by" {
  description = "Tool managing these resources"
  type        = string
  default     = "opentofu-idi"
}
"@

            $AllVariables = @($StandardVars) + $VariableBlocks
        }

        # ── Assemble final HCL ────────────────────────────────────────────
        $MainHCL = $MainBlocks -join "`n"
        $VariablesHCL = if ($IncludeVariables) { $AllVariables -join "`n" } else { '' }
        $OutputsHCL = if ($IncludeOutputs -and $OutputBlocks.Count -gt 0) {
            @"
# =============================================================================
# Outputs — Auto-generated from IDI IntentGraph
# =============================================================================
$($OutputBlocks -join "`n")
"@
        } else { '' }

        # ── Write or return ───────────────────────────────────────────────
        $Result = [PSCustomObject]@{
            intent_hash    = $IntentGraph.intent_hash
            provider       = $Provider
            environment    = $Environment
            strategy       = $Strategy
            backend        = $Backend
            resources      = $Resources.Count
            modules_used   = @($ModulesUsed.Keys)
            files          = @{}
            generated_at   = [DateTime]::UtcNow.ToString('o')
        }

        if ($Format -eq 'JSON') {
            $Result.files = @{
                'main.tf.json'      = _Convert-HCLToJSON $MainHCL
                'variables.tf.json' = _Convert-HCLToJSON $VariablesHCL
                'outputs.tf.json'   = _Convert-HCLToJSON $OutputsHCL
            }
        } else {
            $Result.files = @{
                'main.tf'      = $MainHCL
                'variables.tf' = $VariablesHCL
                'outputs.tf'   = $OutputsHCL
            }
        }

        if ($OutputPath -and -not $DryRun) {
            if (-not (Test-Path $OutputPath)) {
                New-Item -ItemType Directory -Path $OutputPath -Force | Out-Null
            }

            foreach ($file in $Result.files.GetEnumerator()) {
                if ($file.Value) {
                    $FilePath = Join-Path $OutputPath $file.Key
                    if ($PSCmdlet.ShouldProcess($FilePath, "Write HCL file")) {
                        Set-Content -Path $FilePath -Value $file.Value -Encoding UTF8
                        Write-Host "  📄 $($file.Key)" -ForegroundColor Green
                    }
                }
            }

            # Write generation metadata
            $MetaPath = Join-Path $OutputPath '.idi-generation.json'
            @{
                intent_hash  = $IntentGraph.intent_hash
                intent       = $IntentGraph.intent
                provider     = $Provider
                environment  = $Environment
                strategy     = $Strategy
                generated_at = $Result.generated_at
                resources    = $Resources.Count
                modules_used = @($ModulesUsed.Keys)
            } | ConvertTo-Json -Depth 5 | Set-Content -Path $MetaPath -Encoding UTF8
        } elseif ($DryRun) {
            Write-Host "`n  📋 DRY RUN — Would generate:" -ForegroundColor Yellow
            foreach ($file in $Result.files.GetEnumerator()) {
                if ($file.Value) {
                    $LineCount = ($file.Value -split "`n").Count
                    Write-Host "    $($file.Key): $LineCount lines" -ForegroundColor Gray
                }
            }
        }

        Write-Host "`n  ✅ Generated $($Result.files.Count) files for $Provider/$Environment ($Strategy strategy)" -ForegroundColor Green

        if ($PassThru) { return $Result }
    }
}


