function Get-TargetInfo {
    <#
    .SYNOPSIS
        Parse target string to extract user, host, and port
    #>
    param([string]$TargetString)
    
    $result = @{
        User = $null
        Host = $TargetString
        Port = 22
        IsWindows = $false
    }
    
    # Parse user@host format
    if ($TargetString -match "^(.+)@(.+)$") {
        $result.User = $Matches[1]
        $result.Host = $Matches[2]
    }
    
    # Parse host:port format
    if ($result.Host -match "^(.+):(\d+)$") {
        $result.Host = $Matches[1]
        $result.Port = [int]$Matches[2]
    }
    
    # Detect if Windows (by port or hostname)
    if ($result.Port -eq 5985 -or $result.Port -eq 5986) {
        $result.IsWindows = $true
    }
    
    return $result
}

function Invoke-SSHCommand {
    <#
    .SYNOPSIS
        Execute command on remote Linux host via SSH
    #>
    param(
        [string]$Host,
        [string]$Command,
        [string]$User = "root",
        [int]$Port = 22,
        [string]$KeyPath,
        [PSCredential]$Credential
    )
    
    $sshArgs = @("-o", "StrictHostKeyChecking=no", "-o", "BatchMode=yes")
    
    if ($KeyPath) {
        $sshArgs += @("-i", $KeyPath)
    }
    
    $sshArgs += @("-p", $Port)
    $sshArgs += "${User}@${Host}"
    $sshArgs += $Command
    
    if ($DryRun) {
        Write-Host "[DRY RUN] ssh $($sshArgs -join ' ')" -ForegroundColor Yellow
        return
    }
    
    $result = & ssh @sshArgs 2>&1
    if ($LASTEXITCODE -ne 0) {
        throw "SSH command failed: $result"
    }
    return $result
}

function Copy-FileToRemote {
    <#
    .SYNOPSIS
        Copy file to remote host via SCP
    #>
    param(
        [string]$LocalPath,
        [string]$RemotePath,
        [string]$Host,
        [string]$User = "root",
        [int]$Port = 22,
        [string]$KeyPath
    )
    
    $scpArgs = @("-o", "StrictHostKeyChecking=no", "-o", "BatchMode=yes")
    
    if ($KeyPath) {
        $scpArgs += @("-i", $KeyPath)
    }
    
    $scpArgs += @("-P", $Port)
    $scpArgs += $LocalPath
    $scpArgs += "${User}@${Host}:${RemotePath}"
    
    if ($DryRun) {
        Write-Host "[DRY RUN] scp $($scpArgs -join ' ')" -ForegroundColor Yellow
        return
    }
    
    & scp @scpArgs
    if ($LASTEXITCODE -ne 0) {
        throw "SCP failed"
    }
}

function Deploy-ToLinux {
    <#
    .SYNOPSIS
        Deploy AitherMesh to Linux target
    #>
    param(
        [hashtable]$TargetInfo,
        [string]$Mode,
        [switch]$GPUEnabled,
        [string]$Registry
    )
    
    $host_ = $TargetInfo.Host
    $user = $TargetInfo.User ?? "root"
    $port = $TargetInfo.Port
    
    Write-Host "`n🐧 Deploying to Linux: $user@$host_`:$port" -ForegroundColor Cyan
    
    # Create temp install script
    $scriptPath = Join-Path $env:TEMP "aither-install.sh"
    $LinuxInstallScript | Set-Content -Path $scriptPath -Force
    
    # Copy script to remote
    Write-Host "  Copying install script..."
    Copy-FileToRemote -LocalPath $scriptPath `
        -RemotePath "/tmp/aither-install.sh" `
        -Host $host_ -User $user -Port $port `
        -KeyPath $SSHKeyPath
    
    # Execute script
    Write-Host "  Running installation..."
    $gpuFlag = if ($GPUEnabled) { "true" } else { "false" }
    Invoke-SSHCommand -Host $host_ -User $user -Port $port -KeyPath $SSHKeyPath `
        -Command "chmod +x /tmp/aither-install.sh && sudo /tmp/aither-install.sh '$Mode' '$gpuFlag' '$Registry'"
    
    # Generate and copy mesh config
    Write-Host "  Configuring mesh..."
    $hostname = Invoke-SSHCommand -Host $host_ -User $user -Port $port -KeyPath $SSHKeyPath `
        -Command "hostname"
    
    $meshConfig = $MeshConfigTemplate -f @(
        $hostname.Trim(),
        $Role,
        $Controller,
        $Registry,
        $gpuFlag
    )
    
    $configPath = Join-Path $env:TEMP "mesh.conf"
    $meshConfig | Set-Content -Path $configPath -Force
    
    Copy-FileToRemote -LocalPath $configPath `
        -RemotePath "/etc/aither/mesh.conf" `
        -Host $host_ -User $user -Port $port `
        -KeyPath $SSHKeyPath
    
    # Store PSK securely
    if ($MeshPSK) {
        Write-Host "  Storing mesh PSK..."
        $pskHash = [System.BitConverter]::ToString(
            [System.Security.Cryptography.SHA256]::Create().ComputeHash(
                [System.Text.Encoding]::UTF8.GetBytes($MeshPSK)
            )
        ).Replace("-", "").ToLower()
        
        Invoke-SSHCommand -Host $host_ -User $user -Port $port -KeyPath $SSHKeyPath `
            -Command "echo '$MeshPSK' | sudo tee /etc/aither/secrets/mesh.psk > /dev/null && sudo chmod 600 /etc/aither/secrets/mesh.psk"
    }
    
    # Start mesh agent
    Write-Host "  Starting mesh agent..."
    try {
        Invoke-SSHCommand -Host $host_ -User $user -Port $port -KeyPath $SSHKeyPath `
            -Command "cd /var/lib/aither && python3 /usr/local/bin/aither-mesh-agent.py join --controller '$Controller' --role '$Role' 2>&1 || echo 'Agent start pending...'"
    } catch {
        Write-Warning "Mesh agent may need manual start. Run: aither-mesh-agent join"
    }
    
    Write-Host "`n✅ Linux deployment complete!" -ForegroundColor Green
}

function Deploy-ToWindows {
    <#
    .SYNOPSIS
        Deploy AitherMesh to Windows target via WinRM
    #>
    param(
        [hashtable]$TargetInfo,
        [string]$Mode,
        [switch]$GPUEnabled,
        [string]$Registry,
        [PSCredential]$Credential
    )
    
    $host_ = $TargetInfo.Host
    
    Write-Host "`n🪟 Deploying to Windows: $host_" -ForegroundColor Cyan
    
    # Create remote session
    $sessionParams = @{
        ComputerName = $host_
    }
    
    if ($Credential) {
        $sessionParams.Credential = $Credential
    }
    
    if ($TargetInfo.Port -eq 5986) {
        $sessionParams.UseSSL = $true
    }
    
    if ($DryRun) {
        Write-Host "[DRY RUN] Would connect to $host_ via WinRM" -ForegroundColor Yellow
        return
    }
    
    $session = New-PSSession @sessionParams
    
    try {
        # Run install script
        $gpuFlag = if ($GPUEnabled) { "true" } else { "false" }
        $result = Invoke-Command -Session $session -ScriptBlock {
            param($Script, $Mode, $GPU, $Reg)
            $scriptBlock = [scriptblock]::Create($Script)
            & $scriptBlock $Mode $GPU $Reg
        } -ArgumentList $WindowsInstallScript, $Mode, $gpuFlag, $Registry
        
        Write-Host $result
        
        # Configure mesh
        $hostname = Invoke-Command -Session $session -ScriptBlock { hostname }
        
        $meshConfig = $MeshConfigTemplate -f @(
            $hostname,
            $Role,
            $Controller,
            $Registry,
            $gpuFlag
        )
        
        Invoke-Command -Session $session -ScriptBlock {
            param($Config, $PSK)
            $Config | Set-Content -Path "$env:ProgramData\Aither\mesh.conf" -Force
            if ($PSK) {
                $PSK | Set-Content -Path "$env:ProgramData\Aither\secrets\mesh.psk" -Force
            }
        } -ArgumentList $meshConfig, $MeshPSK
        
        Write-Host "`n✅ Windows deployment complete!" -ForegroundColor Green
        
    } finally {
        Remove-PSSession $session
    }
}

